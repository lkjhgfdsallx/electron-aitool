/**
 * postinstall 脚本：为 chromium-bidi 创建 lib/cjs/ 兼容层
 *
 * playwright-core (v1.52.x) 的 coreBundle.js 中硬编码了以下 require 路径：
 *   - chromium-bidi/lib/cjs/bidiMapper/BidiMapper
 *   - chromium-bidi/lib/cjs/cdp/CdpConnection
 *
 * 但 chromium-bidi v12+ 的目录结构已变为 lib/bidiMapper/（不再有 cjs 子目录）。
 * 本脚本在 pnpm install 后自动创建 lib/cjs/ 目录，包含 re-export 包装文件，
 * 确保 playwright-core 运行时能正确加载 chromium-bidi。
 *
 * 这些文件会被 pnpm install 重新生成，因此可以安全地添加到 .gitignore。
 */

const fs = require('fs');
const path = require('path');

// target: 要创建的 shim 文件路径（相对于 chromium-bidi 包根目录）
// source: shim 文件中 require() 的真实模块路径（相对于 target 所在目录）
const mappings = [
  {
    target: 'lib/cjs/bidiMapper/BidiMapper.js',
    source: '../../bidiMapper/BidiMapper.js'
  },
  {
    target: 'lib/cjs/cdp/CdpConnection.js',
    source: '../../cdp/CdpConnection.js'
  }
];

function run() {
  // 查找 chromium-bidi 包的实际安装路径
  let bidiPkgPath;
  try {
    bidiPkgPath = require.resolve('chromium-bidi/package.json');
  } catch {
    // chromium-bidi 未安装，跳过
    console.log('[postinstall] chromium-bidi not found, skipping CJS shim creation');
    return;
  }

  const bidiDir = path.dirname(bidiPkgPath);

  for (const { target, source } of mappings) {
    const targetPath = path.join(bidiDir, target);
    // source 是相对于 target 文件所在目录的
    const sourcePath = path.resolve(path.dirname(targetPath), source);

    // 检查源文件是否存在
    if (!fs.existsSync(sourcePath)) {
      console.warn(`[postinstall] Source not found: ${sourcePath}, skipping`);
      continue;
    }

    // 如果目标文件已存在且不是我们创建的包装文件，跳过
    if (fs.existsSync(targetPath)) {
      const content = fs.readFileSync(targetPath, 'utf-8');
      if (content.includes('// chromium-bidi CJS compatibility shim')) {
        // 已经是我们创建的，继续更新
      } else {
        console.log(`[postinstall] ${target} already exists (not a shim), skipping`);
        continue;
      }
    }

    // 创建目录
    const targetDir = path.dirname(targetPath);
    fs.mkdirSync(targetDir, { recursive: true });

    // 写入 re-export 包装文件
    const shimContent = `// chromium-bidi CJS compatibility shim\n// Auto-generated by scripts/postinstall-chromium-bidi-cjs.cjs\n// playwright-core expects chromium-bidi/lib/cjs/ paths, but v12+ uses lib/ directly\nmodule.exports = require('${source}');\n`;
    fs.writeFileSync(targetPath, shimContent, 'utf-8');
    console.log(`[postinstall] Created CJS shim: ${target}`);
  }
}

run();
